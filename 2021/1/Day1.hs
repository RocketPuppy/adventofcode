import Debug.Trace

-- Define reverse function application to make things neater
import Prelude hiding ((<$))
a <$ f = f a

-- Here's a catamorphism. It's basically a universal fold.
-- Any F-algebra can be used with a catamorphism.
-- An F-algebra is a type implementing Functor, an initial object, and an operation on the type
type Algebra f a = f a -> a -- the generic f-algebras
newtype Fix f = Iso { invIso :: f (Fix f) } -- gives us the initial algebra for the functor f
cata :: Functor f => Algebra f a -> (Fix f -> a) -- catamorphism from Fix f to a
cata alg = alg . fmap (cata alg) . invIso -- note that invIso and alg map in opposite directions

data SonarScan a c = InitialPing a | Ping a c   -- Here's the type for our f-algebra, parameterizing on c allows for the operation to happen
instance Functor (SonarScan a) where
    fmap f (InitialPing i) = InitialPing i
    fmap f (Ping i s) = Ping i (f s)

type SonarScanA a = Fix (SonarScan a) -- It turns out the fixed point of a type is it's initial algebra

-- Some constructors to build pings
initialPing :: a -> SonarScanA a
initialPing = Iso . InitialPing

ping :: a -> SonarScanA a -> SonarScanA a
ping i s = Iso $ Ping i s

-- Here's the operation for algebra. A different operation would give a different algebra.
-- It transforms pings into a running rate with the last ping.
depthRate :: (Ord a, Num a) => Algebra (SonarScan a) (a, a)
depthRate (InitialPing d) = (0, d)
depthRate (Ping d' (i, d)) = if d' > d then (1 + i, d') else (i, d')

